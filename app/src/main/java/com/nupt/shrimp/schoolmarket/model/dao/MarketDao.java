package com.nupt.shrimp.schoolmarket.model.dao;

/**
 * 商店DAO类
 */
public class MarketDao {
    
    private int id;
    private String name;
    private String pic;

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getPicture() {
        return pic;
    }
    public void setPicture(String pic) {
        this.pic = pic;
    }

}
